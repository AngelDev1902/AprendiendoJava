public class Operadores_3 {
    public static void main(String[] args) {
        /*
            Operadores aritmeticos

            Los operadores aritmeticos son aquellos que nos permiten realizar operaciones aritmeticas.
            Estos operadores son los siguientes:
            - + : Suma dos valores
            - - : Resta dos valores
            - * : Multiplica dos valores
            - / : Divide dos valores
            - % : Devuelve el resto de la division de dos valores
        */

        /*
            Operadores de asignacion

            Los operadores de asignacion son aquellos que nos permiten asignar un valor a una variable.
            Estos operadores son los siguientes:
            - = : Asigna el valor de la derecha a la variable de la izquierda
            - += : Suma el valor de la derecha al valor de la variable de la izquierda y asigna el resultado a la variable de la izquierda
            - -= : Resta el valor de la derecha al valor de la variable de la izquierda y asigna el resultado a la variable de la izquierda
            - *= : Multiplica el valor de la derecha al valor de la variable de la izquierda y asigna el resultado a la variable de la izquierda
            - /= : Divide el valor de la derecha al valor de la variable de la izquierda y asigna el resultado a la variable de la izquierda
            - %= : Asigna el resto de la division del valor de la derecha al valor de la variable de la izquierda y asigna el resultado a la variable de la izquierda
        */

        /*
            Operadores de incremento y decremento

            Los operadores de incremento y decremento son aquellos que nos permiten incrementar o decrementar en una unidad el valor de una variable.
            Estos se dividen en dos, los operadores de pre-incremento y pre-decremento y los operadores de post-incremento y post-decremento.
            Los operadores de pre-incremento y pre-decremento son aquellos que incrementan o decrementan en una unidad el valor de una variable y luego
            devuelven el valor de la variable,
            mientras que los operadores de post-incremento y post-decremento son aquellos que devuelven el valor de una variable y luego incrementan o decrementan

            ejemplo:
            int a = 10;
            int b = ++a; // b = 11, a = 11
            int c = 10;
            int d = c++; // d = 10, c = 11
        */

        /*
            Operadores de comparacion

            Los operadores de comparacion son aquellos que nos permiten comparar dos valores.
            Estos operadores son los siguientes:
            - == : Compara si dos valores son iguales
            - != : Compara si dos valores son distintos
            - > : Compara si el valor de la izquierda es mayor al valor de la derecha
            - < : Compara si el valor de la izquierda es menor al valor de la derecha
            - >= : Compara si el valor de la izquierda es mayor o igual al valor de la derecha
            - <= : Compara si el valor de la izquierda es menor o igual al valor de la derecha
        */

        /*
            Operadores logicos

            Los operadores logicos son aquellos que nos permiten realizar operaciones logicas.
            Estos operadores son los siguientes:
            - && : Devuelve true si ambos valores son true (AND)
            - || : Devuelve true si alguno de los valores es true (OR)
            - ! : Devuelve true si el valor es false y false si el valor es true (NOT)

            tiene prioridad el operador && sobre el operador ||
            y tiene prioridad el operador || sobre el operador !
        */

        /*
            Operador Instanceof

            El operador instanceof nos permite saber si un objeto es instancia de una clase o de una interfaz.
            Este operador es el siguiente:
            - instanceof : Devuelve true si el objeto es instancia de la clase o interfaz especificada
        */

        /*
            Precedencia de operadores:

            1. Parentesis
            2. Incremento y decremento
            3. Multiplicacion, division y resto
            4. Suma y resta
            5. Operadores de comparacion
            6. Operadores logicos
            7. Operadores de asignacion
        */

    }
}
